    @Override
    public void run(){
        Assets.init();
        init();
        int fps = 60;
        double timePerTick = 1000000000 / fps;
        double delta = 0;
        long now;
        long lastTime = System.nanoTime();
        long timer = 0;
    
    
        while(running){
            now = System.nanoTime();
            delta += (now - lastTime) / timePerTick;
            timer += now - lastTime;
            lastTime = now;
        
            if(delta >= 1){
                tick();
                render();
                ticks++;
                delta--;
            }
        
            if(timer >= 1000000000){
                System.out.println("FPS: " + ticks);
                ticks = 0;
                timer = 0;
            }
        }
    }
    
    public void tick(){
        if(!handler.getCurrentScreen().isPauseGame()){
            if(!handler.isGameWon()){
                world.tick(ticks);
            }
            if(handler.getScore() > highScore){
                highScore = handler.getScore();
            }
        }
        
        handler.getCurrentScreen().tick();
    }
    
    public void render(){
        display.setBf(display.getCanvas().getBufferStrategy());
        if(display.getBf() == null){
            display.getCanvas().createBufferStrategy(3);
            return;
        }
        display.setG((Graphics2D) display.getBf().getDrawGraphics());
        
        Graphics2D g = display.getG();
        g.clearRect(0, 0, display.getWidth(), display.getHeight());
        
        g.fillRect(0, 0, display.getWidth(), display.getHeight());
        
        world.render(g);
        
        handler.getCurrentScreen().render(g);
        
        display.getBf().show();
        g.dispose();
    }
